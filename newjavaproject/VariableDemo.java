package newjavaproject;

public class VariableDemo {
	public static void main(String[] args) {
		int intVar;
		long longVar = 123L;
		float floatVar;
		double doubleVar;
		char charVar;
		boolean booleanVar;
		
		intVar = 10;
		floatVar = 12.34f;
		doubleVar =23.45;
		charVar = 'A';
		booleanVar = true;
		System.out.println("values ="+ longVar + " "+ intVar +" "+ floatVar +" "+ doubleVar + " "+ charVar +" " + booleanVar);
	}
}
